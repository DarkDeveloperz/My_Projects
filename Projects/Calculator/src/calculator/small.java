    private void c12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_c12MouseClicked
       new Thread()
{
    public void run()
    {
        int a = 0,b = 0;
char c[] = tf1.getText().toCharArray();
for(int i=0; i<c.length; i++)
{
    if(c[i]=='(')
    {
        a++;
    }
    if(c[i]==')')
    {
        b++;
    }
}
 if(a == b)
    {
        tf1.setText(tf1.getText()+"*(");
        a=0;
        b=0;
    }
    else
    {
        tf1.setText(tf1.getText()+")");
        a=0;
        b=0;
    }
    }
}.start();
    }//GEN-LAST:event_c12MouseClicked

