package visionfirst_akashtripathi;

/**
 *
 * @author akash
 */
public class ModelTable {
    String sno,c_name,c_add,created_by,status;

    public ModelTable(String sno, String c_name, String c_add, String created_by, String status) {
        this.sno = sno;
        this.c_name = c_name;
        this.c_add = c_add;
        this.created_by = created_by;    
        this.status = status;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getSno() {
        return sno;
    }

    public void setSno(String sno) {
        this.sno = sno;
    }

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    public String getC_add() {
        return c_add;
    }

    public void setC_add(String c_add) {
        this.c_add = c_add;
    }

    public String getCreated_by() {
        return created_by;
    }

    public void setCreated_by(String created_by) {
        this.created_by = created_by;
    }

}
